package com.github.expdev07.blazequeue.bungeecord.queue.priority;

/**
 * A helper class that attempts to standardize how priorities are defined.
 */
public final class Priority {

    /**
     * Highest priority possible.
     */
    public static final int HIGHEST = Integer.MIN_VALUE;

    /**
     * Lowest priority possible.
     */
    public static final int LOWEST = Integer.MAX_VALUE;

    /**
     * No priority.
     */
    public static final int NONE = 0;

}
